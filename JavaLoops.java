public class JavaLoops
{
   public static void main(String[] args) 
   {
	   int[] numbers = {4,5,6,7,8,9,10};
      
      for (int i = 0; i < numbers.length; i++)
        System.out.println(numbers[i]);
        
      for (int i = 0; i < numbers.length; i++)
         if (i % 2 == 0) 
            System.out.print(numbers[i]+ " ");
            
      
      

         
           

         
   }
}